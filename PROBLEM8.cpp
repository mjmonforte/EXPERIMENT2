    cout << "Multiplication table of " << num << ':' << endl;
    
    q = 1 * num;
    w = 2 * num;
    e = 3 * num;
    r = 4 * num;
    t = 5 * num;
    y = 6 * num;
    u = 7 * num;
    i = 8 * num;
    o = 9 * num;
    p = 10 * num;
    
    cout << "1 * " << num << " = " << q << endl;
    cout << "2 * " << num << " = " << w << endl;
    cout << "3 * " << num << " = " << e << endl;
    cout << "4 * " << num << " = " << r << endl;
    cout << "5 * " << num << " = " << t << endl;
